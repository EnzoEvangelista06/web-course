/** Operadores lógicos:
 *      && -> AND -> E
 *      || -> OR -> OU
 *      ! -> NOT -> NÃO (inversão)
 *  
 */

console.log(10 >= 10 && 15 < 10); // true -> Todas as comparações são verdadeira.
console.log(10 > 10 && 15 < 10); // false -> Apenas uma comparação é verdadeira.

console.log(10 > 10 || 15 < 10); // true -> uma expressão é verdadeira.

// inversão de resultado
console.log(!(10 > 10 || 5 === 9)); // true -> fizemos a inversão do resuldo.



