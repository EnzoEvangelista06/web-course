/** Operadores de comparação:
 *      MAIOR QUE: >
 *      MAIOR QUE OU IGUAL A: >=
 *      MENOR QUE: <
 *      MENOR QUE OU IGUAL A: <=
 *      IGUAL: ==
 *      IDÊNTICO: === (valor e tipo)
 *      DIFERENTE: !=
 *      NÃO IDÊNTICO: !== (valor e tipo)
 *  
 */

console.log(10 >= 5);
console.log(10 < 5);

console.log('10' === 10); // false -> mesmo valor, diferente no tipo
console.log('10' == 10); // true -> valores iguais

console.log('10' != 10); // false -> valores iguais 
console.log('10' !== 10); // true -> valores iguais, tipos diferentes

